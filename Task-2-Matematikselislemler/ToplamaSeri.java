public class Toplamaseri {

    public static void main(String[] args) {

        // 1'den 9'a kadar olan sayıları doğrudan topluyoruz
        // ve sonucu 'toplam' adında bir değişkende saklıyoruz.
        int toplam = 1 + 2 + 3 + 4 + 5 + 6 + 7 + 8 + 9;

        // Sonucu ekrana yazdırıyoruz.
        System.out.println("İşlemin sonucu: " + toplam);
    }
}
